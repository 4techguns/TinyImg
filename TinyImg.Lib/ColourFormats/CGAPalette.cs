namespace TinyImg.Lib.ColourFormats
{
    public enum CGAPalette : byte
    {
        // I = 0
        Black = 0x00,
        Blue = 0x01,
        Green = 0x02,
        Cyan = 0x03,
        Red = 0x04,
        Magenta = 0x05,
        Brown = 0x06,
        LightGrey = 0x07,

        // I = 1
        DarkGrey = 0x08,
        LightBlue = 0x09,
        LightGreen = 0x0A,
        LightCyan = 0x0B,
        LightRed = 0x0C,
        LightMagenta = 0x0E,
        Yellow = 0x0F,
        White = 0x10
    }
}
